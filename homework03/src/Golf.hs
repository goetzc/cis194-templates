-- |
--
-- >>> localMaxima [2,9,5,6,1]
-- [9,6]
-- >>> localMaxima [2,3,4,1,5]
-- [4]
-- >>> localMaxima [1,2,3,4,5]
-- []

localMaxima :: [Integer] -> [Integer]
localMaxima (x:x2:x3:xs)
  | x < x2 && x2 > x3 = x2 : localMaxima (x2:x3:xs)
  | otherwise         = localMaxima (x2:x3:xs)
localMaxima _ = []

